public class operatorassigment2 {
    public static void main(String[] args) {
        int a = 15;

        a+= 6;
        System.out.println("value a [15] +=6 = " + a);

        int b = 8;
        b-= 2;
        System.out.println("value b [8] -=2 =  " + b);

        int c = 3;
        c*= 4;
        System.out.println("value c [3] *=4 = " + c);
    }
}
